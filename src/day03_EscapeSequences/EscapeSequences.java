package day03_EscapeSequences;
/*
\n : start a new line
\t :Tab (paragraph space)
\\ : Single Backslash \
\" : Double quote "


 */
public class EscapeSequences {

    public static void main(String[] args) {
        System.out.println("Java \nPython \nC#"); //example of \n

        System.out.println("-------------------------------------------------");

        System.out.println("Hello Cydeo! \nHow are you all today? \nIt's nice to see you all! \nWhat class do we have today");

        System.out.println("\tJava is Cool Programming Language"); //example of \t
        System.out.println("    Java is cool Programming Language");

        System.out.println("--------------------------------------------------");

        System.out.println("/ \\"); //example of forward slash / and backslash \. in order to print one backslash \ , we need to give 2 backslashes\\

        System.out.println("---------------------------------------------------------------------");

        System.out.println(" My favorite TV show is \"Game of Throne\"");//Example of double quote. \"






    }



}
