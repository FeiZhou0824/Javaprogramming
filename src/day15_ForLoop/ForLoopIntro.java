package day15_ForLoop;

public class ForLoopIntro {

    public static void main(String[] args) {

        System.out.println("Hello World!"); //如果重复打印Hello World 10次 ，或者100次可以用 forloop来解决，而不用自己打印10次，100次。
        System.out.println("Hello World!");
        System.out.println("Hello World!");
        System.out.println("Hello World!");
        System.out.println("Hello World!");
        System.out.println("Hello World!");
        System.out.println("Hello World!");
        System.out.println("Hello World!");
        System.out.println("Hello World!");
        System.out.println("Hello World!");

        System.out.println("---------------------------------------");
        //i stand for initialization.
        for(int i = 1; i<=10; i++ ){
            System.out.println("Hello World");
        }












    }
}
